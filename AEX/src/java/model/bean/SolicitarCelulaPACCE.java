package model.bean;

/**
 *
 * @author ncs
 */
public class SolicitarCelulaPACCE {
    private int id;
    private String nome;
    private String nomeArticulador;
    private String descricao;
    private String email;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNomeArticulador() {
        return nomeArticulador;
    }

    public void setNomeArticulador(String nomeArticulador) {
        this.nomeArticulador = nomeArticulador;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
}
